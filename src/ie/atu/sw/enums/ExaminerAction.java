package ie.atu.sw.enums;

/**
 * Enumerates the possible actions that an examiner can take in response to an examination paper.
 * This enum includes actions such as approval or rejection of the paper by the examiner.
 */
public enum ExaminerAction {
    /**
     * Represents the action of approving an examination paper.
     */
    APPROVE,

    /**
     * Represents the action of rejecting an examination paper.
     */
    REJECT
}
